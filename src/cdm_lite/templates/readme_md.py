import datetime


def generate_readme(cdm_version: str, generated_at: str = ""):

    if not generated_at:
        generated_at = datetime.datetime.now(tz=datetime.UTC).isoformat()

    template = """
# cdm-models {cdm_version}

Pydantic v2 models generated from the
[FINOS Common Domain Model](https://github.com/finos/common-domain-model)
JSON Schema v{cdm_version}.

Generated by [cdm-lite](https://github.com/ben05allen/cdm-lite) on {generated_at}.

## Usage

```python
from cdm_event_common_TradeState_schema import TradeState

trade_state = TradeState.model_validate_json(json_str)
```

## Compatibility

These models were generated from CDM JSON Schema v{cdm_version} and are
not guaranteed to be compatible with other CDM versions.
"""

    return template.format(cdm_version=cdm_version, generated_at=generated_at)
